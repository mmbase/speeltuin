package nl.kennisnet.cpb.webservice.error;

/**
 * 
 * Thrown when a node in the cloud with the specified number was not present
 * Log: not needed
 * 
 * @author dekok01
 * @version $Revision: 1.0
 */
public class EntryNotFoundException extends WebserviceException {

   public EntryNotFoundException(String message) {
      super(message);
   }

   public EntryNotFoundException(Throwable cause) {
      super(cause);
   }

}
