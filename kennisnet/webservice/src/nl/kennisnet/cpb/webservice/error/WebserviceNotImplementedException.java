package nl.kennisnet.cpb.webservice.error;

/**
 * Application Exception thrown in case a HTTP method was invoked on a resource
 * that does not support such a method
 * 
 * Log: not needed
 * 
 * @author dekok01
 * @version $Revision: 1.0
 */
public class WebserviceNotImplementedException extends WebserviceException {

   public WebserviceNotImplementedException(Throwable cause) {
      super(cause);
   }

   public WebserviceNotImplementedException(String message) {
      super(message);
   }
}
