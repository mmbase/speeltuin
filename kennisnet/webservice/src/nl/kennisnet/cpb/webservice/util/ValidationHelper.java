package nl.kennisnet.cpb.webservice.util;

/**
 * Utility class for validating the input to the webservice
 * 
 * Log: not needed
 * 
 * @author dekok01
 * @version $Revision: 1.0
 */
public class ValidationHelper {

   /** Check whether the code is a single token containing only a-zA-Z_0-9
    * @param code the code to be validated
    * @return if the code is valid
    */
   public static boolean validateToken(String code) {      
      return code.matches("[\\w*]+");      
   }
}
