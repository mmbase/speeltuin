package nl.kennisnet.cpb.webservice.domain;

import java.util.List;

import org.mmbase.bridge.Node;

/**
 * A wrapper for a gallerij. Contains a list of images 
 * 
 * @author dekok01
 * @version $Revision: 1.0
 */
public class GallerijWrapper extends NodeWrapper {

   private List<ImageWrapper> images;

   public GallerijWrapper(Node node, List<ImageWrapper> images) {
      super(node);
      this.images = images;
   }
   
   public List<ImageWrapper> getImages() {
      return images;
   }  
}
